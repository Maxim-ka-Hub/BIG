// Функція для пошуку елемента в масиві periodic_table
function searchElement() {
   var userInput = document.getElementById('elementInput').value;
   var resultWindow = document.getElementById('result');
   
   var foundElement = null;

   // Пошук за назвою, позначенням або атомною масою
   for (var key in periodic_table) {
       var elementData = periodic_table[key];
       if (elementData.includes(userInput) || key == userInput || elementData[2] == userInput) {
           foundElement = elementData;
           break;
       }
   }
   
   if (foundElement) {
       resultWindow.innerHTML = '<strong>Назва:</strong> ' + foundElement[0] + ', <strong>Символ:</strong> ' + foundElement[1] + ', <strong>Атомний номер:</strong> ' + key;
   } else {
       resultWindow.innerHTML = 'Елемент не знайдено';
   }
}